def calculate_life_number(dob):
    try:
        digits = [int(d) for d in dob if d.isdigit()]
        total = sum(digits)
        while total > 9:
            total = sum(int(d) for d in str(total))
        return total if total != 0 else 9
    except:
        return random.randint(1, 9)

